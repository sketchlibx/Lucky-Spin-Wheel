package sketchlib.lucky.spinwheel;

public class LuckyItem {
    public String topText;
    public int icon;
    public int color;

    // Empty Constructor
    public LuckyItem() {}

    public LuckyItem(String text, int icon, int color) {
        this.topText = text;
        this.icon = icon;
        this.color = color;
    }
}
